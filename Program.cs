using System;

namespace Fizzbuzz {

class Program  {

public static void Main(); {

public string Fizzbuzz(int number)
{
    var answer = "";

    if (number % 3 == 0)
    {
        answer = "fizz";
    }
    if (number % 5 == 0)
    {
        answer = "buzz"
    
    }
    else
    {
        answer = "fizzbuzz"
    }
}   
}
}
}


